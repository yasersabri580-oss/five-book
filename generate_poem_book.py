def build_document(output_path: str) -> None:
    doc = Document()

    # Page setup: A4, wider margins
    section = doc.sections[0]
    section.page_width = Cm(21)
    section.page_height = Cm(29.7)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2.5)
    section.top_margin = Cm(2.5)
    section.bottom_margin = Cm(2.5)

    # Set document-level RTL
    doc.settings.element.append(OxmlElement("w:mirrorMargins"))

    # ── Cover page ────────────────────────────────────────────────────────────
    cover_p = doc.add_paragraph()
    set_rtl_paragraph(cover_p)
    cover_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cover_run = cover_p.add_run("🌸 کتاب شعر کودکان 🌸")
    set_rtl_run(cover_run)
    cover_run.font.size = Pt(28)
    cover_run.font.bold = True
    cover_run.font.color.rgb = RGBColor(0x1A, 0x53, 0x76)

    sub_p = doc.add_paragraph()
    set_rtl_paragraph(sub_p)
    sub_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = sub_p.add_run("برای کودکان ۷ تا ۱۲ ساله")
    set_rtl_run(sub_run)
    sub_run.font.size = Pt(16)
    sub_run.font.italic = True

    desc_p = doc.add_paragraph()
    set_rtl_paragraph(desc_p)
    desc_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    desc_run = desc_p.add_run(
        "این کتاب شامل شعرهای زیبا، توضیح ساده، داستان و واژه‌نامه است"
    )
    set_rtl_run(desc_run)
    desc_run.font.size = Pt(13)

    doc.add_page_break()

    # ── Poems ─────────────────────────────────────────────────────────────────
    for i, poem_data in enumerate(POEMS, start=1):
        add_poem_section(doc, poem_data, i)

    doc.save(output_path)
    print(f"✅ poem.docx saved to: {output_path}")


if __name__ == "__main__":
    import os
    output = os.path.join(os.path.dirname(__file__), "poem.docx")
    build_document(output)
