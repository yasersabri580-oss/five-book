def set_rtl_paragraph(paragraph):
    """Force paragraph direction to RTL."""
    pPr = paragraph._p.get_or_add_pPr()
    bidi = OxmlElement("w:bidi")
    bidi.set(qn("w:val"), "1")
    pPr.append(bidi)
    jc = OxmlElement("w:jc")
    jc.set(qn("w:val"), "right")
    pPr.append(jc)


def set_rtl_run(run):
    """Force run direction to RTL."""
    rPr = run._r.get_or_add_rPr()
    rtl = OxmlElement("w:rtl")
    rPr.append(rtl)


def add_title(doc, text, level=1):
    """Add a styled title paragraph."""
    p = doc.add_paragraph()
    set_rtl_paragraph(p)
    run = p.add_run(text)
    set_rtl_run(run)
    if level == 1:
        run.font.size = Pt(22)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0x1A, 0x53, 0x76)
    elif level == 2:
        run.font.size = Pt(16)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0x2E, 0x7D, 0x32)
    elif level == 3:
        run.font.size = Pt(13)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0x6A, 0x1B, 0x9A)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p


def add_body_paragraph(doc, text, bold=False, italic=False, size=12):
    """Add a regular RTL body paragraph."""
    p = doc.add_paragraph()
    set_rtl_paragraph(p)
    run = p.add_run(text)
    set_rtl_run(run)
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    p.paragraph_format.space_before = Pt(3)
    p.paragraph_format.space_after = Pt(3)
    return p


def add_poem_text(doc, poem_text):
    """Add poem lines with indentation and poetry styling."""
    for line in poem_text.split("\n"):
        p = doc.add_paragraph()
        set_rtl_paragraph(p)
        run = p.add_run(line)
        set_rtl_run(run)
        run.font.size = Pt(13)
        run.font.italic = True
        run.font.color.rgb = RGBColor(0x1A, 0x1A, 0x6E)
        p.paragraph_format.left_indent = Cm(1)
        p.paragraph_format.space_before = Pt(2)
        p.paragraph_format.space_after = Pt(2)


def add_vocab_table(doc, vocab_list):
    """Add a 2-column RTL vocabulary table."""
    # Header row + data rows
    table = doc.add_table(rows=1 + len(vocab_list), cols=2)
    table.style = "Table Grid"
    table.alignment = WD_TABLE_ALIGNMENT.RIGHT

    # Header
    hdr_cells = table.rows[0].cells
    for cell, text in zip(hdr_cells, ["معنی ساده", "واژه"]):
        cell.text = ""
        p = cell.paragraphs[0]
        set_rtl_paragraph(p)
        run = p.add_run(text)
        set_rtl_run(run)
        run.font.bold = True
        run.font.size = Pt(12)
        run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
        # Header background color
        tc = cell._tc
        tcPr = tc.get_or_add_tcPr()
        shd = OxmlElement("w:shd")
        shd.set(qn("w:val"), "clear")
        shd.set(qn("w:color"), "auto")
        shd.set(qn("w:fill"), "1A5376")
        tcPr.append(shd)

    # Data rows  (vocab list is (word, meaning); table cols are [meaning, word] for RTL)
    for i, (word, meaning) in enumerate(vocab_list):
        row = table.rows[i + 1]
        for cell, text in zip(row.cells, [meaning, word]):
            cell.text = ""
            p = cell.paragraphs[0]
            set_rtl_paragraph(p)
            run = p.add_run(text)
            set_rtl_run(run)
            run.font.size = Pt(11)
            # Alternate row color
            if i % 2 == 0:
                tc = cell._tc
                tcPr = tc.get_or_add_tcPr()
                shd = OxmlElement("w:shd")
                shd.set(qn("w:val"), "clear")
                shd.set(qn("w:color"), "auto")
                shd.set(qn("w:fill"), "EBF5FB")
                tcPr.append(shd)


def add_section_divider(doc):
    """Add a thin horizontal rule as a paragraph."""
    p = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single")
    bottom.set(qn("w:sz"), "4")
    bottom.set(qn("w:space"), "1")
    bottom.set(qn("w:color"), "AAAAAA")
    pBdr.append(bottom)
    pPr.append(pBdr)
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(8)


def add_poem_section(doc, poem_data, poem_number):
    """Add all sections for one poem."""

    # ── Poem title ────────────────────────────────────────────────────────────
    add_title(doc, f"شعر {poem_number}: {poem_data['title']}", level=1)
    add_body_paragraph(doc, f"شاعر: {poem_data['author']}", italic=True, size=11)

    doc.add_paragraph()

    # ── Original poem ─────────────────────────────────────────────────────────
    add_title(doc, "📖 متن شعر", level=2)
    add_poem_text(doc, poem_data["poem"])

    doc.add_paragraph()

    # ── Vocabulary table ──────────────────────────────────────────────────────
    add_title(doc, "📚 جدول واژگان", level=2)
    add_vocab_table(doc, poem_data["vocab"])

    doc.add_paragraph()

    # ── Explanation ───────────────────────────────────────────────────────────
    add_title(doc, "💡 توضیح شعر (برای بچه‌ها)", level=2)
    add_body_paragraph(doc, poem_data["explanation"], size=12)

    doc.add_paragraph()

    # ── Story ─────────────────────────────────────────────────────────────────
    add_title(doc, "📝 داستان", level=2)
    for line in poem_data["story"].split("\n"):
        add_body_paragraph(doc, line, size=12)

    doc.add_paragraph()

    # ── Summary ───────────────────────────────────────────────────────────────
    add_title(doc, "✅ خلاصه و درس شعر", level=2)
    add_body_paragraph(doc, poem_data["summary"], size=12)

    add_section_divider(doc)
    doc.add_page_break()


