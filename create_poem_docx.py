def create_poem_docx():
    """Create the complete DOCX file with all poems and educational content"""
    doc = Document()
    
    # Add main title
    title = doc.add_heading('کتاب اشعار کودکانه', level=0)
    set_rtl(title)
    
    for i, poem in enumerate(poems_data):
        # Add poem number and title
        heading = doc.add_heading(f'{i+1}. {poem["title"]}', level=1)
        set_rtl(heading)
        
        # Section: Original Poem
        add_rtl_paragraph(doc, "📜 متن شعر:", bold=True, font_size=14)
        for verse in poem["verses"]:
            add_rtl_paragraph(doc, verse, font_size=12)
        
        doc.add_paragraph()
        
        # Section: Vocabulary Table
        add_rtl_paragraph(doc, "📚 واژه‌نامه (برای کودکان):", bold=True, font_size=14)
        create_vocab_table(doc, poem["vocab"])
        
        doc.add_paragraph()
        
        # Section: Simple Explanation
        add_rtl_paragraph(doc, "💡 توضیح ساده‌ی شعر:", bold=True, font_size=14)
        add_rtl_paragraph(doc, poem["explanation"], font_size=12)
        
        doc.add_paragraph()
        
        # Section: Short Story
        add_rtl_paragraph(doc, "📖 داستان کوتاه (برگرفته از شعر):", bold=True, font_size=14)
        add_rtl_paragraph(doc, poem["story"], font_size=12)
        
        doc.add_paragraph()
        
        # Section: Summary
        add_rtl_paragraph(doc, "✨ خلاصه:", bold=True, font_size=14)
        add_rtl_paragraph(doc, poem["summary"], font_size=12)
        
        # Add page break between poems (except for last one)
        if i < len(poems_data) - 1:
            doc.add_page_break()
    
    # Save the document
    output_path = "/home/runner/work/five-book/five-book/poem.docx"
    doc.save(output_path)
    print(f"Document saved successfully to: {output_path}")

if __name__ == "__main__":
    create_poem_docx()
