from docx import Document
from docx.shared import Pt, Inches, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_TABLE_ALIGNMENT
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

def set_rtl(paragraph):
    """Set right-to-left direction for a paragraph"""
    pPr = paragraph._p.get_or_add_pPr()
    bidi = OxmlElement('w:bidi')
    bidi.set(qn('w:val'), '1')
    pPr.append(bidi)
    paragraph.alignment = WD_ALIGN_PARAGRAPH.RIGHT

def add_rtl_paragraph(doc, text, bold=False, font_size=12):
    """Add a right-to-left paragraph"""
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(font_size)
    run.font.name = 'Arial'
    set_rtl(p)
    return p

def create_vocab_table(doc, vocab_data):
    """Create vocabulary table with RTL support"""
    table = doc.add_table(rows=1, cols=4)
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.RIGHT
    
    # Header row
    headers = ['واژه', 'سطح', 'معنی ساده', 'مثال']
    header_cells = table.rows[0].cells
    for i, header in enumerate(headers):
        header_cells[i].text = header
        for p in header_cells[i].paragraphs:
            set_rtl(p)
            for run in p.runs:
                run.bold = True
    
    # Data rows
    for row_data in vocab_data:
        row = table.add_row()
        for i, cell_text in enumerate(row_data):
            row.cells[i].text = cell_text
            for p in row.cells[i].paragraphs:
                set_rtl(p)
    
    return table

